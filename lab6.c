void spi_send_16(uint8_t cs_pin, uint16_t data) {
    uint8_t buf[2];
    buf[0] = (data >> 8) & 0xFF;
    buf[1] = data & 0xFF;
    gpio_put(cs_pin, 0);
    spi_write_blocking(spi0, buf, 2);
    gpio_put(cs_pin, 1);
    sleep_us(10);
}

void send_rotation(uint8_t cs_pin, int rotation) {
    for (int row = 0; row < 16; row++) {
        for (int col = 0; col < 16; col++) {
            uint16_t pixel;
            switch (rotation) {
                case 0: pixel = sprite[row * 16 + col]; break;
                case 1: pixel = sprite[col * 16 + (15 - row)]; break;
                case 2: pixel = sprite[(15 - row) * 16 + (15 - col)]; break;
                case 3: pixel = sprite[(15 - col) * 16 + row]; break;
                default: pixel = 0;
            }
            spi_send_16(cs_pin, pixel);
        }
    }
}

